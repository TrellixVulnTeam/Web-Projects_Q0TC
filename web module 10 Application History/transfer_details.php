      		<imp>What is Advanced Standing?</imp>
Advanced Standing is an assessment process used to recognise the skills and knowledge you have achieved, whether that be through study or life/work experience, by
matching it with what would be covered in specific units within a course. If you have successfully completed any of the units in the course in which you are enrolling, or
their equivalent,you may be eligible for advanced standing.
Where advanced standing is granted you will be considered to have already successfully completed the unit(s), and will not have to participate in training or assessment
activitiesfor that unit(s). In determining where advanced standing may be granted, the University must be confident of the currency of the applicant’s knowledge. In fields
where practice/technologyis changing rapidly, advanced standingmay not be granted where knowledge has dated.
As an enrolled International onshore student if your study load falls below the standard full time study load you must attach a completed Application for Reduced Study
Load form.
Australian students should be aware that the granting of advanced standing could affect study load and therefore eligibility for Austudy or Abstudy. If you are an onshore
international student you need to check with the university
Prospective students will need to resubmit applications for Advanced Standing if they fail to enrol at Victoria University within 12 months of receiving notice of approved
credit.
